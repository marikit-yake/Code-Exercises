def mostWordsFound(sentences: List[str]) -> int:
    # Initalize low "max"
    mx = 0
    
    # Compare the length of each sentence to the previous max length (init: 0)
    # Assign the new max length to mx
    for s in sentences:
        mx = max(mx, len(s.split(" ")))
    return mx

